     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        //
        $id = Auth::user()->id;
        $name = $request['name'];
        $email = $request['email'];
        $intro = $request['intro'];
        $photo = $request['photo']->getClientOriginalName();
        $user = User::find($id);

        $user->name = $name;
        $user->profile_intro = $intro;
        $user->profile_picture = $photo;
        $user->email = $email;
    
        
        if($user->save()){
            
            $request->photo->storeAs('/public/uploads',$photo);
            return redirect('/my/profile')->with('status','Profile successfully updated');
        }
        
        
    }

